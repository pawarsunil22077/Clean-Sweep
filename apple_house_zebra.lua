-- "Clean Sweep" in Lua

-- Create a function to clean up the living room.

function clean_livingroom()
  -- Get the living room area ready 
  vacuum_floor()
  dust_shelves()
  dust_furniture()
  remove_garbage()
 
  -- Clean up the windows
  clean_windows()
 
  -- Put away all the toys
  put_away_toys()
 
  -- Make the sofa and chairs look nice
  fluff_pillows()
  fold_blankets()
 
  -- Finish up
  mop_floor()
 end
 
-- Create a function to vacuum the floor
 
function vacuum_floor()
  -- Move all the furniture
  move_chairs()
  move_sofa()
 
  -- Plug in the vacuum cleaner
  plug_in_vacuum()
 
  -- Start vacuuming
  start_vacuuming()
 
  -- Empty the vacuum cleaner
  empty_vacuum()
 
  -- Unplug the vacuum cleaner
  unplug_vacuum()
 end
 
-- Create a function to dust the shelves
 
function dust_shelves()
  -- Get the dusting cloth
  get_dusting_cloth()
 
  -- Take down the items on the shelves
  remove_items()
 
  -- Dust the shelves
  start_dusting()
 
  -- Replace the items
  replace_items()
 end
 
-- Create a function to dust the furniture
 
function dust_furniture()
  -- Get the dusting cloth
