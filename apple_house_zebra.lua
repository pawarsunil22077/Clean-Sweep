-- Create a function to remove garbage
 
function remove_garbage()
  -- Put on cleaning gloves
  put_on_gloves()
 
  -- Collect all the garbage
  collect_garbage()
 
  -- Empty the garbage bags
  empty_garbage_bags()
 end

-- Create a function to clean windows 

function clean_windows()
  -- Get the window cleaning supplies
  get_cleaning_supplies()
 
  -- Wash the inside of the windows
  wash_inside()
 
  -- Wash the outside of the windows
  wash_outside()
 
  -- Dry the windows
  dry_windows()
 end

-- Create a function to put away toys

